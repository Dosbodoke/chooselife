/**
 * Animation constants for consistent spring animations across the app.
 * These values create smooth, non-bouncy transitions.
 */

/**
 * Damping value for spring animations.
 * Higher values = less oscillation/bounce (80 = smooth, no bounce)
 */
export const ANIMATION_DAMPING = 80;

/**
 * Stiffness value for spring animations.
 * Higher values = faster animations
 */
export const ANIMATION_STIFFNESS = 200;
