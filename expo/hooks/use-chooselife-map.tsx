const CHOOSELIFE_BOUNDS: [number, number, number, number] = [
  -47.47092834048479, // sw long
  -13.8732788197745, // sw lat
  -47.44604833769594, // ne long
  -13.841165559933813, // ne lat
];
const PACK_NAME = 'chooselife';

export const useChooselifeMap = async () => {
  const handleDownloadRegion = useCallback(async () => {
    try {
      const packs = await Mapbox.offlineManager.getPacks();
      const existingPack = packs.find((p) => p.name === PACK_NAME);

      if (existingPack) {
        console.log('Pack already downloaded');
        return;
      }

      await offlineManager.createPack(
        {
          name: PACK_NAME,
          styleURL: Mapbox.StyleURL.SatelliteStreet,
          minZoom: 14,
          maxZoom: 20,
          bounds: [
            [CHOOSELIFE_BOUNDS[2], CHOOSELIFE_BOUNDS[3]],
            [CHOOSELIFE_BOUNDS[0], CHOOSELIFE_BOUNDS[1]],
          ],
        },
        (pack, status) => {
          if (status.percentage === 100) {
            console.log(`Finished downloading ${pack.name}`);
          }
        },
      );
    } catch (error) {
      console.error('Error downloading region:', error);
    }

    return true;
  }, []);

  useEffect(() => {
    handleDownloadRegion();
  }, []);
};

type TTrailShape = GeoJSON.Feature<
  LineString,
  {
    name: string;
    color: string;
  }
>;

export const TrailShape: React.FC<{
  shape: TTrailShape;
}> = ({ shape }) => {
  const camera = useAtomValue(cameraStateAtom);

  const trailMiddleCoordinates =
    shape.geometry.coordinates[
      Math.floor(shape.geometry.coordinates.length / 2)
    ];
  return (
    <>
      <Mapbox.ShapeSource
        id={`${shape.properties.name}-source`}
        shape={shape}
        hitbox={{ width: 42, height: 42 }}
      >
        <Mapbox.LineLayer
          id={`${shape.properties.name}-layer}`}
          style={{
            lineWidth: 3,
            lineCap: 'round',
            lineJoin: 'round',
            lineColor: shape.properties.color,
          }}
        />
      </Mapbox.ShapeSource>

      {trailMiddleCoordinates && camera.zoom >= 14 && (
        <Mapbox.MarkerView
          coordinate={[trailMiddleCoordinates[0], trailMiddleCoordinates[1]]}
          anchor={{ x: 0.5, y: 1 }} // Anchor slightly above the point (adjust y)
          allowOverlap={true} // Allow tooltip to overlap markers/other elements
        >
          <View className="relative overflow-hidden rounded-md px-2 py-1">
            <View className="absolute inset-0 opacity-50 bg-slate-950"></View>
            <Text className="text-sm text-white">{shape.properties.name}</Text>
          </View>
        </Mapbox.MarkerView>
      )}
    </>
  );
};

